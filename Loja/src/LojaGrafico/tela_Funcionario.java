    public void criaTabela(){
    
    DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
    
    
     Funcionario.carregarFuncionarios();
    
    Object rowData[] = new Object[7];
    
        for (int i=0;i<funcionarios.size();i++) {
            rowData[0] = funcionarios.get(i).id;
            rowData[1] = funcionarios.get(i).nome;
            rowData[2] = funcionarios.get(i).cpf;
            rowData[3] = funcionarios.get(i).telefone;
            rowData[4] = funcionarios.get(i).horario;
            rowData[5] = funcionarios.get(i).contrat;
            rowData[6] = funcionarios.get(i).salario;
            modelo.addRow(rowData);
        }
    }
    
    


